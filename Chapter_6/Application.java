package Java.Java_Tutorial_for_Complete_Beginners.Chapter_6;

public class Application {
    public static void main(String[] args) {
        short myShort = 847; // 16-bit or 2-byte variable
        int myInt = 88; // 32-bit or 4-byte variable
        long myLong = 9797; // 64-bit or 8-byte variable

        float myFloat = 324.3f;
        double myDouble = 7.3243;

        char myChar = 'y';

        boolean myBoolean = true;

        byte myByte = 127; // 8-bit or 1-byte variable

        System.out.println(myShort);
        System.out.println(myInt);
        System.out.println(myLong);
        System.out.println();

        System.out.println(myFloat);
        System.out.println(myDouble);
        System.out.println();

        System.out.println(myChar);
        System.out.println();

        System.out.println(myBoolean);
        System.out.println();

        System.out.println(myByte);
    }
}
